 var index=$("#container").data('highchartsChart');
 var chart=Highcharts.charts[index];
chart.update({ chart: {
        type: 'spline' }
});
chart.update({
        plotOptions: {
            series: {
                marker: {
                    enabled: false,
                    states: {
                        hover: {
                            enabled: false
                        }
                    }
                }, showCheckbox: true,
                selected: true,

                events: {
                    checkboxClick: function (event) {
                        if (this.visible) {
                            this.hide();
                        } else {
                            this.show();
                        }
                    }
                }
            }
        }
    }
);
