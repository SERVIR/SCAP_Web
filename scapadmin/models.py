# -----------------------------------------------------------------
# Emissions model: This model stores pre-calculated emission for each predefined AOI, land cover, and AGB data source
# The objects in this model are created through an automated process to be implemented as part of S-CAP ETLs
# -----------------------------------------------------------------
class Emissions(models.Model):
    # id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False) - Let's use autogenerated IDs
    lc_id = models.ForeignKey(ForestCoverSource, default=1, verbose_name="LandCover", on_delete=models.SET_DEFAULT)
    agb_id = models.ForeignKey(AGBSource, default=1, verbose_name="AGB", on_delete=models.SET_DEFAULT)
    aoi_id = models.ForeignKey(PredefinedAOI, default=1, verbose_name="AOI", on_delete=models.SET_DEFAULT)
    year = models.IntegerField(help_text="Year")
    baseline_year = models.IntegerField(help_text="Baseline Year for emissions calculations", blank=True, null=True)
    lc_agb_value = models.FloatField(help_text="Value")


    class Meta:
        verbose_name_plural = "Emissions"

