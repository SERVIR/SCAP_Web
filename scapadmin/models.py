# ---------------------------------------------------------------------------------------------
# ForestCoverChangeFile model: This model stores the locations of the forest cover change files
# derived by the system. These files are created by the system as part of the ETL process.
# For estimating performance issues, we will store the time it took to generate the file and the
# date and time the file was created.
# ---------------------------------------------------------------------------------------------
class ForestCoverChangeFile(models.Model):
    # Use autogenerated IDs (don't rely on a user defined a primary key)
    fc_source = models.ForeignKey(ForestCoverSource, help_text="Forest Cover Source", on_delete=models.CASCADE)
    year = models.IntegerField(help_text="Year the change was calculated for")
    baseline_year = models.IntegerField(help_text="Baseline year for forest cover change calculation", blank=True, null=True)
    created = models.DateTimeField(help_text='Date and time the file was created', default=datetime.now)
    processing_time = models.FloatField(help_text="Time to generate the file (seconds)", blank=True, null=True)
    file_name = models.CharField(max_length=200, default="", help_text="Forest Cover Change file name")
    file_directory = models.CharField(max_length=200, default="", help_text="Forest Cover Change file directory")


    class Meta:
        verbose_name_plural = "Forest Cover Change Files"

# -----------------------------------------------------------------
# LandCoverChange model: This model stores pre-calculated land cover change data for each predefined AOI (forest gain
# and loss, plus initial forest area for the baseline year)
# Note: Standard units for area measurements pending (hectares, km2, etc.)
# The objects in this model are created through an automated process to be implemented as part of S-CAP ETLs
# -----------------------------------------------------------------
class ForestCoverChange(models.Model):
    fc_source = models.ForeignKey(ForestCoverSource, help_text="Forest Cover Source", on_delete=models.CASCADE)
    aoi = models.ForeignKey(PredefinedAOI, default=1, help_text="AOI the calculation was made for", on_delete=models.CASCADE)
    year = models.IntegerField(help_text="Year the change was calculated for")
    baseline_year = models.IntegerField(help_text="Baseline Year for forest cover change calculation", blank=True, null=True)
    initial_forest_area = models.FloatField(help_text="Initial forest area for baseline year")
    forest_gain = models.FloatField(help_text="Forest Gain", null=True)
    forest_loss = models.FloatField(help_text="Forest Loss")
    processing_time = models.FloatField(help_text="Time to calculate forest change data for this time period, AOI and Forest Cover Change source (seconds)", blank=True, null=True)

    def net_forest_change(self):
        return self.forest_gain - self.forest_loss